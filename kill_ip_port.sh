read -p "Введите IP-адрес и номер порта в формате IP:PORT: " IP_PORT

# Разбиваем ввод пользователя на IP-адрес и номер порта
IP=$(echo "$IP_PORT" | cut -d':' -f1)
PORT=$(echo "$IP_PORT" | cut -d':' -f2)

# Проверяем, используется ли указанный порт на указанном IP-адресе
if echo "" > /dev/tcp/$IP/$PORT; then
  # Получаем идентификатор процесса, использующего порт
  PID=$(lsof -t -i :$PORT)

  # Убиваем процесс
  kill -9 $PID

  echo "Процесс, использующий порт $PORT на IP-адресе $IP, был убит"
else
  echo "Нет процесса, использующего порт $PORT на IP-адресе $IP"
fi

